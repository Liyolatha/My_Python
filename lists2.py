
fruits = ["apple", "banana", "cherry", "kiwi"]

#append method
fruits.append("kiwi")
print(fruits)

#insert method
fruits.insert(1, "orange")
print(fruits)

#remove method
fruits.remove("kiwi")
print(fruits)

#sort method(ascending)
fruits.sort()
print(fruits)

#sort method(descending)
fruits.sort(reverse=True)
print(fruits)
